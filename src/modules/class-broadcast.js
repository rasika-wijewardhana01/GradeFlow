// ═══════════════════════════════════════════════════════════════
//  src/modules/class-broadcast.js
//  One-tap class performance summary for WhatsApp broadcast.
//  Builds a concise text summary from current results:
//   class avg, pass/fail counts, top student, weakest subject.
//  Also exposes openBroadcastModal() for a full preview.
// ═══════════════════════════════════════════════════════════════

function _cbBuildSummary() {
  if (!window.results || !window.results.length) return null;

  const cls       = (document.getElementById('className')    || {}).value?.trim() || 'Class';
  const examLabel = (document.getElementById('examLabel')    || {}).value?.trim() || 'Exam';
  const year      = (document.getElementById('academicYear') || {}).value?.trim() || '';

  const rs = window.results;
  const pcts = rs.map(r => r.pct).filter(p => p !== null && p !== undefined);
  const avg  = pcts.length ? parseFloat((pcts.reduce((a, b) => a + b, 0) / pcts.length).toFixed(1)) : null;

  // Pass / fail using the pass threshold from the grading scale
  const gs      = [...(window.gradingScale || [])].sort((a, b) => b.minPct - a.minPct);
  const passPct = gs.length > 1 ? gs[gs.length - 2].minPct : 50;
  const passed  = rs.filter(r => r.pct !== null && r.pct >= passPct).length;
  const failed  = rs.filter(r => r.pct !== null && r.pct < passPct).length;
  const absent  = rs.filter(r => r.pct === null).length;

  // Top student (rank 1)
  const top = rs.find(r => r.rank === 1);

  // Most-improved subject (highest avg)
  const subjectAvgs = (window.subjects || []).map(sub => {
    const vals = rs.map(r => {
      const v = r.subjMarks?.[sub.name];
      return (v !== undefined && v !== null && v !== 'AB') ? (parseFloat(v) / sub.max * 100) : null;
    }).filter(v => v !== null);
    return { name: sub.name, avg: vals.length ? parseFloat((vals.reduce((a, b) => a + b, 0) / vals.length).toFixed(1)) : null };
  }).filter(s => s.avg !== null);

  const bestSubject  = subjectAvgs.length ? subjectAvgs.reduce((a, b) => a.avg >= b.avg ? a : b) : null;
  const worstSubject = subjectAvgs.length ? subjectAvgs.reduce((a, b) => a.avg <= b.avg ? a : b) : null;

  return { cls, examLabel, year, avg, passed, failed, absent, total: rs.length, top, passPct, bestSubject, worstSubject, subjectAvgs };
}

function _cbFormatText(data) {
  if (!data) return '';
  const { cls, examLabel, year, avg, passed, failed, absent, total, top, bestSubject, worstSubject } = data;

  const lines = [
    `📊 *${cls}${year ? ' — ' + year : ''} | ${examLabel} Results*`,
    '',
    `👥 Total Students: ${total}`,
    avg !== null ? `📈 Class Average: ${avg}%` : '',
    `✅ Passed: ${passed}`,
    `❌ Failed: ${failed}`,
    absent > 0 ? `⚠️ Absent: ${absent}` : '',
    '',
  ].filter(l => l !== undefined);

  if (top) lines.push(`🏆 Top Student: ${top.student} (${top.pct}% — ${top.grade})`);
  if (bestSubject)  lines.push(`💪 Strongest Subject: ${bestSubject.name} (avg ${bestSubject.avg}%)`);
  if (worstSubject && worstSubject.name !== bestSubject?.name) {
    lines.push(`📚 Needs Attention: ${worstSubject.name} (avg ${worstSubject.avg}%)`);
  }

  lines.push('', 'Generated by GradeFlow');
  return lines.join('\n');
}

function openBroadcastModal() {
  if (!window.results?.length) {
    window.toast('Calculate results first before sharing', 'error');
    return;
  }
  const data = _cbBuildSummary();
  const text  = _cbFormatText(data);

  const preview = document.getElementById('cbPreviewText');
  const overlay = document.getElementById('broadcastModalOverlay');
  if (preview) preview.textContent = text;
  if (overlay) overlay.classList.add('open');
}

function closeBroadcastModal() {
  document.getElementById('broadcastModalOverlay')?.classList.remove('open');
}

window.cbShareWhatsApp = function () {
  const data = _cbBuildSummary();
  const text = _cbFormatText(data);
  if (!text) return;
  window.open('https://wa.me/?text=' + encodeURIComponent(text), '_blank');
};

window.cbCopyText = function () {
  const data = _cbBuildSummary();
  const text = _cbFormatText(data);
  if (!text) return;
  navigator.clipboard.writeText(text).then(() => window.toast('Summary copied!', 'success'));
};

Object.assign(window, { openBroadcastModal, closeBroadcastModal });
